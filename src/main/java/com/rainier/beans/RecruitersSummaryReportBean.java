package com.rainier.beans;

public class RecruitersSummaryReportBean {
	private String  recruiters;
	private String recruitersMailId;
	private String assignReportStatus;
	private String candidateApplyId;
	private String jobPostId;
	private String jobTitlename;
	private String candidateName;
	
	
	public String getCandidateName() {
		return candidateName;
	}
	public void setCandidateName(String candidateName) {
		this.candidateName = candidateName;
	}
	public String getRecruiters() {
		return recruiters;
	}
	public void setRecruiters(String recruiters) {
		this.recruiters = recruiters;
	}
	public String getRecruitersMailId() {
		return recruitersMailId;
	}
	public void setRecruitersMailId(String recruitersMailId) {
		this.recruitersMailId = recruitersMailId;
	}
	public String getAssignReportStatus() {
		return assignReportStatus;
	}
	public void setAssignReportStatus(String assignReportStatus) {
		this.assignReportStatus = assignReportStatus;
	}
	public String getCandidateApplyId() {
		return candidateApplyId;
	}
	public void setCandidateApplyId(String candidateApplyId) {
		this.candidateApplyId = candidateApplyId;
	}
	public String getJobPostId() {
		return jobPostId;
	}
	public void setJobPostId(String jobPostId) {
		this.jobPostId = jobPostId;
	}
	public String getJobTitlename() {
		return jobTitlename;
	}
	public void setJobTitlename(String jobTitlename) {
		this.jobTitlename = jobTitlename;
	}
	
	
	

}
